export async function getServerSideProps(context) {
  const genre = context.query.genre;
  const res = await fetch(
    `https://api.themoviedb.org/3${requests[genre]?.url || requests.fetchTrending.url} `)

  const data = await res.json()
  return {
    props: {
      result: data
    }
  };
}
